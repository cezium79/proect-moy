# Документация структуры проекта "Охрана" (Для ИИ)

## 🛠 Стек технологий и архитектура
- **Язык**: Kotlin (100%)
- **UI Фреймворк**: Jetpack Compose (Декларативный интерфейс)
- **Локальное хранилище**: SharedPreferences (управление через SharedPrefsManager)
- **Архитектура**: Плоская структура (Single Package / Feature-by-File), экраны связаны через MainActivity.
- **Ключевой функционал**: Сканирование и обработка QR-кодов (встроенные модели ML Kit / Barcode), генерация отчетов об обходе.

## 📁 Назначение ключевых файлов (`app/src/main/main/java/com/example/ohrana/`)

### 🎨 Экраны интерфейса (Compose Screens)
- `PrivetScreen.kt` — Стартовый приветственный экран приложения.
- `AdministratorScreen.kt` — Панель управления для администратора системы.
- `EmployeeListScreen.kt` — Экран со списком сотрудников (вывод, добавление, менеджмент персонала).
- `OhrannikScreen.kt` — Основное рабочее пространство охранника (интерфейс для дежурства).
- `OhrannikCabinetScreen.kt` — Личный кабинет/профиль конкретного сотрудника охраны.
- `SpisokOtchetov.kt` — Экран просмотра истории сгенерированных отчетов об обходах и чекпоинтах.

### ⚙ Логика, Утилиты и Данные
- `MainActivity.kt` — Главная и единственная Activity. Точка входа, координирующая навигацию между Compose-экранами.
- `Employee.kt` — Data-класс (модель данных), описывающий сущность сотрудника.
- `QrHandler.kt` — Логика распознавания, парсинга и обработки результатов сканирования QR-кодов.
- `SharedPrefsManager.kt` — Утилитный класс для сохранения сессий, авторизации и локальных данных в SharedPreferences.

## 🔄 Порядок взаимодействия компонентов
1. Пользователь стартует на `PrivetScreen`, данные сессии проверяются через `SharedPrefsManager`.
2. В зависимости от роли (Администратор/Охранник) `MainActivity` переключает UI на `AdministratorScreen` или `OhrannikScreen`.
3. Охранник совершает обход, сканируя метки. Данные передаются в `QrHandler`.
4. Результаты проверок и отчетов сохраняются локально и выводятся на экране `SpisokOtchetov`.
